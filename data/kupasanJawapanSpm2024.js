const normalize = (value = '') => String(value).toLowerCase()

const hasKeyword = (content, keywords = []) => (
  keywords.some((keyword) => content.includes(normalize(keyword)))
)

export const getSpm2024PracticeDeck = ({ chapterId, subchapterId, content = '' }) => {
  const normalizedContent = normalize(content)

  const matchedDecks = kupasanJawapanSpm2024Decks.filter((item) => {
    const chapterMatches = item.chapterIds.includes(chapterId)
    const subchapterMatches = !subchapterId || item.subchapterIds.includes(subchapterId)
    const keywordMatches = hasKeyword(normalizedContent, item.keywords)

    return chapterMatches && subchapterMatches && keywordMatches
  })

  if (!matchedDecks.length) return null

  const sourceLabel = [...new Set(matchedDecks.map((item) => item.source))].join(' + ')
  const questions = matchedDecks.flatMap((deck) => (
    deck.questions.map((question) => ({
      ...question,
      source: deck.source,
      paper: deck.paper
    }))
  ))

  return {
    topic: matchedDecks.length === 1 ? matchedDecks[0].topic : 'Past-year practice for this section',
    source: sourceLabel,
    questions
  }
}
